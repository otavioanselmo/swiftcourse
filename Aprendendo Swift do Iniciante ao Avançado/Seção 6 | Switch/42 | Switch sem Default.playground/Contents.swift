// Switches

var letra = "z"

switch letra {
    
case "a":
    print("Esta é a primeira letra do alfabeto.")
    
case "z":
    print("Esta é a última letra do alfabeto.")
    
default:
    print("Qualquer letra.")
}

var careca : Bool = true

switch careca {
    
case true:
    print("Esta pessoa é careca.")
    
case false:
    print("Esta pessoa não é careca.")
}
