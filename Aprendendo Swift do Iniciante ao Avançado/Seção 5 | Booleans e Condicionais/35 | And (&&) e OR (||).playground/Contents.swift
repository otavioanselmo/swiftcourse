// Operadores And (&&) e Or (||)

var idade = 20

idade >= 17
idade <= 24

// AND (&&)

if idade >= 17 && idade <= 24 {
    print("Esta informação está sendo impressa porque toda a condiconal é verdadeira neste caso.")
}

// OR (||)

if idade >= 17 || idade <= 24 {
    print("Este é para o operador OR.")
}
